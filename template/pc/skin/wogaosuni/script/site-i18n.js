  function currentLanguage() {
    var pathMatch = window.location.pathname.match(/^\/(zh|en|ja|ko)(?:\/|$)/i);
    if (pathMatch) {
      return pathMatch[1].toLowerCase();
    }
    var htmlLang = (document.documentElement.lang || "").toLowerCase();
    if (htmlLang === "zh-hant") {
      return "zh";
    }
    if (supported[htmlLang]) {
      return htmlLang;
    }
    return "cn";
  }

  function translateValue(value, dict) {
    if (!value) {
      return value;
    }
    var leading = value.match(/^\s*/)[0];
    var trailing = value.match(/\s*$/)[0];
    var trimmed = value.trim();
    if (dict[trimmed]) {
      return leading + dict[trimmed] + trailing;
    }
    return leading + trimmed + trailing;
  }

  function translateAttributes(root, dict) {
    var attrs = ["placeholder", "aria-label", "title", "data-title"];
    var nodes = root.querySelectorAll("*");
    for (var i = 0; i < nodes.length; i++) {
      var node = nodes[i];
      for (var j = 0; j < attrs.length; j++) {
        var attr = attrs[j];
        if (node.hasAttribute(attr)) {
          node.setAttribute(attr, translateValue(node.getAttribute(attr), dict));
        }
      }
    }
  }

  function translateTextNodes(root, dict) {
    var skip = {
      SCRIPT: true,
      STYLE: true,
      NOSCRIPT: true,
      TEXTAREA: true,
      CODE: true,
      PRE: true
    };
    var walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
      acceptNode: function (node) {
        if (!node.nodeValue || !node.nodeValue.trim()) {
          return NodeFilter.FILTER_REJECT;
        }
        var parent = node.parentElement;
        if (!parent || skip[parent.tagName]) {
          return NodeFilter.FILTER_REJECT;
        }
        if (parent.closest("[data-no-i18n], .topbar-language__panel")) {
          return NodeFilter.FILTER_REJECT;
        }
        return NodeFilter.FILTER_ACCEPT;
      }
    });
    var node;
    while ((node = walker.nextNode())) {
      node.nodeValue = translateValue(node.nodeValue, dict);
    }
  }

  function applyLanguage() {
    var lang = currentLanguage();
    var dict = dictionaries[lang];
    if (!dict) {
      return;
    }
    document.documentElement.setAttribute("data-ui-language", lang);
    translateAttributes(document.body, dict);
    translateTextNodes(document.body, dict);

    var label = document.querySelector("[data-current-lang-label]");
    if (label && supported[lang]) {
      label.textContent = supported[lang];
    }
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", applyLanguage);
  } else {
    applyLanguage();
  }
